import { stringify } from 'qs';
import request from '@/utils/request';
import { http } from '@/utils/request_axios';

// POST请求时，通用处理body参数
function getBodyParams(params) {
  let p = new URLSearchParams();
  for (let i in params) {
    if (typeof params[i] === 'string' || typeof params[i] === 'number') {
      p.append(i, params[i] || '');
    } else if (typeof params[i] === 'object') {
      p.append(i, JSON.stringify(params[i]));
    }
  }
  return p;
}

export async function scrapyLogin(params) {
  const _params = getBodyParams(params);
  return await http.post(`/api/user/login`, _params);
}

